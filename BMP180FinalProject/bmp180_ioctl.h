#ifndef __BMP180_IOCTL_H__
#define __BMP180_IOCTL_H__

#include <linux/ioctl.h>

#define BMP180_MAGIC 0x2B

#define BMP180_GET_TEMP 0x70
#define BMP180_GET_PRES 0x71
#define BMP180_GET_ALTI 0x72
#define BMP180_GET_OSS  0x73
#define BMP180_SET_OSS  0x74
#define BMP180_COEF     0x75
#define BMP180_DBGMSG   0x76

#define BMP180_ACTIVATE_VENTILATION   0x77
#define BMP180_DEACTIVATE_VENTILATION 0x78
#define BMP180_SET_SUSPENSION         0x79
#define BMP180_INCREASE_PRESSURE      0x7A
#define BMP180_DECREASE_PRESSURE      0x7B
#define BMP180_GET_SPEED              0x7C

#define BMP180_ACTIVATE_COOLING       0x7D
#define BMP180_ACTIVATE_HEATING       0x7E
#define BMP180_DEACTIVATE_HVAC        0x7F

/* Định nghĩa các lệnh ioctl */
#define IOCTL_BMP180_GET_TEMP         _IOR(BMP180_MAGIC, BMP180_GET_TEMP, char*)
#define IOCTL_BMP180_GET_PRES         _IOR(BMP180_MAGIC, BMP180_GET_PRES, char*)
#define IOCTL_BMP180_GET_ALTI         _IOR(BMP180_MAGIC, BMP180_GET_ALTI, char*)
#define IOCTL_BMP180_GET_OSS          _IOR(BMP180_MAGIC, BMP180_GET_OSS, char*)
#define IOCTL_BMP180_SET_OSS          _IOW(BMP180_MAGIC, BMP180_SET_OSS, char*)
#define IOCTL_BMP180_COEF             _IO(BMP180_MAGIC, BMP180_COEF)
#define IOCTL_BMP180_DBGMSG           _IO(BMP180_MAGIC, BMP180_DBGMSG)
#define IOCTL_BMP180_ACTIVATE_VENTILATION   _IO(BMP180_MAGIC, BMP180_ACTIVATE_VENTILATION)
#define IOCTL_BMP180_DEACTIVATE_VENTILATION _IO(BMP180_MAGIC, BMP180_DEACTIVATE_VENTILATION)
#define IOCTL_BMP180_SET_SUSPENSION         _IOW(BMP180_MAGIC, BMP180_SET_SUSPENSION, int)
#define IOCTL_BMP180_INCREASE_PRESSURE      _IO(BMP180_MAGIC, BMP180_INCREASE_PRESSURE)
#define IOCTL_BMP180_DECREASE_PRESSURE      _IO(BMP180_MAGIC, BMP180_DECREASE_PRESSURE)
#define IOCTL_BMP180_GET_SPEED              _IOR(BMP180_MAGIC, BMP180_GET_SPEED, double)


#define IOCTL_BMP180_ACTIVATE_COOLING       _IO(BMP180_MAGIC, BMP180_ACTIVATE_COOLING)
#define IOCTL_BMP180_ACTIVATE_HEATING       _IO(BMP180_MAGIC, BMP180_ACTIVATE_HEATING)
#define IOCTL_BMP180_DEACTIVATE_HVAC        _IO(BMP180_MAGIC, BMP180_DEACTIVATE_HVAC)

#endif /* __BMP180_IOCTL_H__ */
